#include <stdio.h> // Включаем стандартную библиотеку ввода-вывода для printf.

// --- Объявления (прототипы) функций ---
// Эти строки сообщают компилятору о существовании функций, их сигнатуре (имя,
// тип возвращаемого значения, типы аргументов). Это позволяет компилятору
// проверить корректность вызовов функций до того, как он увидит их определения.

// Прототип функции 'greet'.
// Тип возвращаемого значения: void (не возвращает ничего).
// Тип аргументов: void (не принимает аргументов).
void greet(void);

// Прототип функции 'square'.
// Тип возвращаемого значения: int (возвращает целое число).
// Тип аргументов: int n (принимает одно целое число 'n').
int square(int n);

// Прототип функции 'calculate_area'.
// Тип возвращаемого значения: double (возвращает число с плавающей точкой двойной точности).
// Тип аргументов: double radius (принимает одно число с плавающей точкой 'radius').
double calculate_area(double radius);

// --- Главная функция программы ---
// int main() является точкой входа в любую C программу.
// Выполнение программы начинается именно отсюда.
int main() {
    // 1. Вызов функции 'greet()'.
    // Эта функция не принимает аргументов и ничего не возвращает,
    // поэтому просто вызываем её по имени.
    greet();

    // 2. Вызов функции 'square()'.
    // Передаем целое число 5 в качестве аргумента.
    // Функция возвращает int, который мы затем используем в printf.
    // %d - спецификатор формата для вывода целых чисел.
    printf("Квадрат 5: %d\n", square(5));

    // 3. Вызов функции 'calculate_area()'.
    // Передаем число с плавающей точкой 3.0 в качестве аргумента.
    // Функция возвращает double.
    // %.2lf - спецификатор формата для вывода double с двумя знаками после запятой.
    printf("Площадь круга радиусом 3.0: %.2lf\n", calculate_area(3.0));

    // Возвращаем 0, чтобы указать операционной системе,
    // что программа завершилась успешно.
    return 0;
}

// --- Определения функций ---
// Здесь содержится фактическая реализация каждой функции.

// Определение функции 'greet'.
// Просто выводит приветственное сообщение на консоль.
void greet(void) {
    printf("Добро пожаловать в мир функций C!\n");
}

// Определение функции 'square'.
// Принимает целое число 'n' и возвращает его квадрат.
int square(int n) {
    return n * n; // Возвращаем произведение n на n.
}

// Определение функции 'calculate_area'.
// Принимает радиус 'radius' типа double.
// Вычисляет площадь круга по формуле PI * radius * radius.
// Для PI используется приближенное значение.
double calculate_area(double radius) {
    const double PI = 3.14159; // Объявляем локальную константу PI.
    return PI * radius * radius; // Возвращаем вычисленную площадь.
}