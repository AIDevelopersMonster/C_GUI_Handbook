#include <stdio.h> // Включаем стандартную библиотеку ввода-вывода для printf.

// Объявление функции (прототип).
// Это сообщает компилятору о том, что функция 'sum' существует.
// Она принимает два целочисленных аргумента (int a, int b) и
// возвращает одно целочисленное значение (int).
int sum(int a, int b);

// Определение функции 'sum'.
// Здесь содержится фактическая логика функции.
// Функция принимает два числа, складывает их и возвращает результат.
int sum(int a, int b) {
    return a + b; // Возвращаем сумму 'a' и 'b'.
}

// Главная функция программы.
// Точка входа в программу. Выполнение начинается отсюда.
int main() {
    // Вызов функции 'sum'.
    // Мы передаем числа 3 и 4 в качестве аргументов.
    // Возвращаемое значение (результат сложения) присваивается переменной 'result'.
    int result = sum(3, 4);

    // Вывод результата на консоль.
    // %d - это спецификатор формата для вывода целого числа.
    // \n - это символ новой строки, который переносит курсор на следующую строку.
    printf("Сумма: %d\n", result);

    // Возвращаем 0, чтобы указать операционной системе,
    // что программа завершилась успешно.
    return 0;
}