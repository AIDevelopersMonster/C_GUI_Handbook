#include <stdio.h> // Включаем стандартную библиотеку ввода/вывода.
                   // Необходима для функции printf().

void main (void)   // Главная функция программы.
{
float a;           // Объявляем переменную 'a' типа float (число с плавающей точкой).
float b = 4.678;   // Объявляем переменную 'b' типа float и инициализируем ее значением 4.678.
int c;             // !!! ВНИМАНИЕ: Объявляем переменную 'c' типа int (целое число).
                   // Это приведет к потере дробной части при присваивании результата.

a = 3.234;         // Присваиваем переменной 'a' значение 3.234.

c = a + b;         // Выполняем сложение 'a' (3.234) и 'b' (4.678), результат которого равен 7.912.
                   // Поскольку переменная 'c' имеет тип int, происходит *неявное преобразование типов*.
                   // Дробная часть результата (0.912) будет отброшена, и 'c' получит значение 7.
                   // Это называется "усечением" (truncation).

printf ("The sum of adding %f and %f is %d\n", a, b, c);
                   // Выводим результат на консоль.
                   // Обратите внимание на спецификаторы формата:
                   // %f для 'a' и 'b' (т.к. они float).
                   // %d для 'c' (т.к. она int).
                   // Вывод для 'c' будет 7, а не 7.912.
}
