/**
 * @file math_demo_commented.c
 * @brief Демонстрационная программа, показывающая объявление переменных и
 * выполнение основных арифметических операций в языке C.
 *
 * Эта программа иллюстрирует:
 * - Различные базовые типы данных (int, float)
 * - Инициализацию переменных
 * - Арифметические операторы (+, -, *, /, %)
 * - Отличие целочисленного деления от деления с плавающей точкой
 * - Использование функции printf для вывода форматированных данных
 */

#include <stdio.h> // Директива препроцессора:
                   // Включает стандартную библиотеку ввода/вывода (Standard Input/Output).
                   // Она содержит функции для работы с вводом (например, scanf) и выводом (например, printf).

int main() {       // Главная функция программы.
                   // Каждая программа на C начинается с выполнения функции main().
                   // int означает, что функция возвращает целое число (обычно 0 для успешного выполнения).

    // --- Объявление и инициализация переменных ---
    // Переменные - это именованные области в памяти, где хранятся данные.
    // int (integer) предназначен для хранения целых чисел.
    // float (floating-point) предназначен для хранения чисел с десятичной частью (с плавающей точкой).

    int a = 10;    // Объявляем целочисленную переменную 'a' и присваиваем ей значение 10.
    int b = 3;     // Объявляем целочисленную переменную 'b' и присваиваем ей значение 3.

    float x = 10.0; // Объявляем переменную 'x' типа float и инициализируем ее значением 10.0.
                    // Важно использовать ".0" для обозначения, что это число с плавающей точкой,
                    // чтобы избежать целочисленной интерпретации.
    float y = 3.0;  // Объявляем переменную 'y' типа float и инициализируем ее значением 3.0.


    // --- Выполнение и вывод результатов арифметических операций для целых чисел ---

    // Оператор сложения (+)
    printf("a + b = %d\n", a + b); // Выводит сумму 'a' и 'b'.
                                   // %d - это спецификатор формата для вывода целых чисел.
                                   // \n - это символ новой строки, который переводит курсор на следующую строку.

    // Оператор вычитания (-)
    printf("a - b = %d\n", a - b); // Выводит разность 'a' и 'b'.

    // Оператор умножения (*)
    printf("a * b = %d\n", a * b); // Выводит произведение 'a' и 'b'.

    // Оператор целочисленного деления (/)
    printf("a / b = %d\n", a / b); // Выводит результат деления 'a' на 'b'.
                                   // В C, если оба операнда деления целые, результат тоже будет целым.
                                   // Дробная часть отбрасывается (происходит усечение), например, 10 / 3 = 3.

    // Оператор остатка от деления (%)
    printf("a %% b = %d\n", a % b); // Выводит остаток от деления 'a' на 'b'.
                                   // Например, 10 разделенное на 3 дает 3 и остаток 1.
                                   // Для вывода символа '%' внутри printf нужно использовать '%%'.


    // --- Выполнение и вывод результата деления для чисел с плавающей точкой ---

    // Деление чисел с плавающей точкой
    printf("x / y = %.2f\n", x / y); // Выводит результат деления 'x' на 'y'.
                                   // %f - спецификатор формата для вывода чисел float.
                                   // .2 перед f указывает, что нужно вывести число с двумя знаками после запятой.
                                   // Например, 10.0 / 3.0 = 3.333333..., но будет выведено 3.33.

    return 0; // Возвращает 0 из функции main, что является стандартным способом
              // сообщить операционной системе об успешном завершении программы.
}