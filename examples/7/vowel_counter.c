// vowel_counter.c
// Пример: Программа для подсчета количества гласных (на английском языке) в введенной строке.

#include <stdio.h>  // Для функций ввода-вывода (printf, fgets).
#include <string.h> // Для строковых функций (strcspn, strlen - хотя strlen не используется напрямую в цикле, но полезна).
#include <ctype.h>  // Для функции tolower() или toupper() - для упрощения проверки регистра.

int main() {
    // Объявляем символьный массив 'str' для хранения введенной строки.
    // Размер 100 позволяет хранить до 99 символов + завершающий '\0'.
    char str[100];
    // Объявляем целочисленную переменную для подсчета гласных, инициализируем нулем.
    int count = 0;

    printf("Введите строку: "); // Запрос ввода у пользователя.

    // Безопасное чтение строки с использованием fgets().
    // Считывает до sizeof(str)-1 символов или до '\n'.
    fgets(str, sizeof(str), stdin);

    // Удаляем символ новой строки '\n', если он был прочитан fgets().
    // Это гарантирует, что строка правильно завершается нулевым символом.
    str[strcspn(str, "\n")] = 0;

    // Цикл for для итерации по каждому символу строки.
    // Условие str[i] != '\0' гарантирует, что цикл продолжается до конца строки.
    // Переменная 'i' автоматически увеличивается (i++).
    for (int i = 0; str[i] != '\0'; i++) {
        // Получаем текущий символ строки.
        char c = str[i];

        // !!! Улучшение: Преобразование символа в нижний регистр для упрощения проверки.
        // Функция tolower() из <ctype.h> преобразует символ в нижний регистр,
        // если он является буквой. Если это не буква, символ возвращается без изменений.
        // Это позволяет проверять только гласные в нижнем регистре, не дублируя код для заглавных.
        c = tolower(c);

        // Проверяем, является ли текущий символ гласной (a, e, i, o, u).
        // Используем логический оператор ИЛИ (||) для проверки каждого условия.
        if (c == 'f' || c == 'e' || c == 'i' || c == 'o' || c == 'u') {
            count++; // Если символ является гласной, увеличиваем счетчик.
        }
    }

    // Выводим общее количество найденных гласных.
    printf("Количество гласных: %d\n", count);

    // Возвращаем 0 для успешного завершения программы.
    return 0;
}

